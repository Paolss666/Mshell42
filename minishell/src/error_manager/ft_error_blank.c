#include "../headers/minishell.h"

int	ft_error_blank(char *str)
{
	int	i;

	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '\0')
	{
		return (999);
	}
	return (0);
}
