#include "../headers/minishell.h"

void	purify(char *str)
{
	char	c;
	int		i;

	i = 0;
	c = '*';
	while (str[i] != '\0')
	{
		if ((str[i] == '\'' || str[i] == '\"') && c == '*')
			c = str[i];
		if (c != '*')
		{
			while (str[i] != '\0' && str[i + 1] && str[i] != c)
			{
				str[i] = '*';
				i++;
			}
			if (str[i] && str[i] == 'c')
				c = '*';
		}
		i++;
	}
}
