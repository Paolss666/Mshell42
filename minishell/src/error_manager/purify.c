#include "../headers/minishell.h"

void	purify(char *str)
{
	char	c;
	int		i;

	i = 0;
	c = '*';
	while (str[i] != '\0')
	{
		if ((str[i] == '\'' || str[i] == '\"') && c == '*')
		{
			c = str[i];
			i++;
		}
		while (c != '*' && str[i] != c && str[i] != '\0')
		{
			str[i] = '*';
			i++;
		}
		if (str[i] != '\0')
			c = '*';
		i++;
	}
}
