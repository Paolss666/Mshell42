#include "libft.h"

int	ft_nblen(int n)
{
	int	nblen;

	nblen = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		nblen++;
	while (n)
	{
		n = n / 10;
		nblen++;
	}
	return (nblen);
}
